#include "libft.h"

void				*ft_memalloc(size_t size)
{
	unsigned char	*mem;

	mem = (unsigned char*)malloc(sizeof(unsigned char) * size);
	if (mem && size)
	{
		ft_bzero(mem, size);
		return ((void *)mem);
	}
	return ((void *)0);
}
